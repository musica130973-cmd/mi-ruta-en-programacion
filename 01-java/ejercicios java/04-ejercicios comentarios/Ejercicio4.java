// Crea un bucle for que imprima los números del 1 al 5. Añade comentarios explicando qué hace el bucle.

public class Ejercicio4 {

    public static void main(String[] args) {

        //Bucle for que imprime los números del 1 al 5
        //Mientras i sea menor o igual a 5 incrementar i en 1
        for(int i = 1; i <= 5; i++){

            // Imprime el valor de i y un espacio en cada iteración del bucle for
            System.out.print(i + " ");
        }
    }
}
