// Ejercicio 7
// Preincremento

public class Ejercicio7 {
    public static void main(String[] args) {
        int a = 5;      // a empieza valiendo 5
        int b = ++a;    //  primero se incrementa a (pasa a 6) y luego ese valor se asigna a b
        System.out.println(a);  // imprime 6
        System.out.println(b);  // imprime 6
    }
}
