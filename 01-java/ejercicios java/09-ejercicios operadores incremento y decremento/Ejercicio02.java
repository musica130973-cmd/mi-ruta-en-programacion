// Ejercicio 2
// Preincremento y Postincremento

public class Ejercicio2 {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x++);    //  Primero imprime x = 10 y después incrementa x en 1 x = 11
        System.out.println(++x);    //  Incrementa x antes de usarlo x pasa de 11 a 12 Imprime 12

        // x++ devuelve el valor antes de sumar 1
        // ++x devuelve el valor después de sumar 1
    }
}
