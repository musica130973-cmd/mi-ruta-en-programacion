// Ejercicio 8
// Indica si cada valor es un tipo de dato primitivo o no primitivo

let a = 10;
let b = "Hola";
let c = false;
let d = {nombre: "Ana"};
let e = [1, 2, 3];
let f = null;

console.log(typeof a);  // number si es primitivo
console.log(typeof b);  // string si es primitivo
console.log(typeof c);  // boolean si es primitivo
console.log(typeof d);  // object  no es primitivo
console.log(typeof e);  // object  no es primitivo
console.log(typeof f);  // object  si es primitivo aunque typeof diga object

